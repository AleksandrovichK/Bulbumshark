package com.company;

class CRectangle {
    private int top, left, width, height;

    CRectangle(int left, int top, int width, int height){
            this.left=left;
            this.top=top;
            this.width=width;
            this.height=height;
        }

    int getTop() {

            return top;
    }
    int getLeft() {

            return left;
    }
    int getWidth() {

        return width;
    }
    int getHeight() {

        return height;
    }
}
